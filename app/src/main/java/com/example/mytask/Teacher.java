package com.example.mytask;

public class Teacher {
    private String name;
    private String phone;
    int age;
    public void setName(String name) {
        this.name = name;
    }
    public void setPhone(String phone) {
        this.phone = phone;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public String getName() {
        return name;
    }
    public String getPhone() {
        return phone;
    }
    public int getAge() {
        return age;
    }
}
